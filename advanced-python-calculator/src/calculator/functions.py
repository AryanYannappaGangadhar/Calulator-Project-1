def sin(x):
    import math
    return math.sin(x)

def cos(x):
    import math
    return math.cos(x)

def tan(x):
    import math
    return math.tan(x)

def log(x, base=math.e):
    import math
    return math.log(x, base)

def exp(x):
    import math
    return math.exp(x)

def sqrt(x):
    import math
    return math.sqrt(x)

def factorial(n):
    import math
    return math.factorial(n)

def power(base, exponent):
    return base ** exponent

def radians(degrees):
    import math
    return math.radians(degrees)

def degrees(radians):
    import math
    return math.degrees(radians)